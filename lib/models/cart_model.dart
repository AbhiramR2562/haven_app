class CartModel {
  final String id;
  final String name;
  final String description;
  final String category;
  final String details;
  final double price;
  final String imageUrl;

  CartModel({
    required this.id,
    required this.name,
    required this.description,
    required this.category,
    required this.details,
    required this.price,
    required this.imageUrl,
  });
}
